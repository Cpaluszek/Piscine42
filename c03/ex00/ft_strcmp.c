int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s2[i] && s1[i])
	{
		if (s1[i] - s2[i] != 0)
			break ;
		i++;
	}
	return (s1[i] - s2[i]);
}
