#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;
	int		neg;

	neg = nb < 0;
	if (nb < 0)
	{
		nb *= -1;
		nb--;
		write(1, "-", 1);
	}
	if (nb >= 10)
	{
		c = '0' + (nb % 10);
		if (neg)
			c++;
		nb /= 10;
	}
	else
	{
		c = '0' + nb;
		nb = 0;
	}
	if (nb != 0)
		ft_putnbr(nb);
	write(1, &c, 1);
}
