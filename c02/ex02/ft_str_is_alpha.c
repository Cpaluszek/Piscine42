int	ft_str_is_alpha(char *str)
{
	char	c;

	c = *str;
	while (c != '\0')
	{
		if (c < 'A' || (c > 'Z' && c < 'a') || c > 'z')
			return (0);
		str++;
		c = *str;
	}
	return (1);
}
